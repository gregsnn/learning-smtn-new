const hora = 12;

if (hora >= 0 && hora <= 11) {
  console.log('Bom dia');
} else if (hora <= 17) {
  console.log('Boa tarde');
} else if (hora <= 23) {
  console.log('Boa noite');
} else {
  console.log('Ola');
}

